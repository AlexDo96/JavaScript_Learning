// Template literals

//Normal way:
const courseName = 'Javascript';
const description = 'Course name: '
    + courseName;

console.log(description);

// Template string: `${variable}`
const courseName_2 = 'Javascript';
const description_2 = `Course name: ${courseName_2}`;

console.log(description_2);

// Multiline strings
const lines = 'Line 1\nLine 2';

console.log(lines);

